function getSafeList(value) {
  return Array.isArray(value) ? value : [];
}

export function generateStaticParams() {
  const params = videos.map((video) => ({ slug: video.slug }));
  if (videos.some((video) => video.slug === CHATGPT_VIDEO_SLUG)) {
    params.push({ slug: 'chatgpt-intro' });
  }
  return params;
}

export async function generateMetadata({ params }) {
  const { slug } = await params;
  const video = getVideo(normalizeVideoSlug(slug));
  if (!video) return {};

  const content = getVideoContent(video);
  return pageMetadata({
    title: content.title,
    description: content.subtitle,
    path: `/videos/${slug}`
  });
}

